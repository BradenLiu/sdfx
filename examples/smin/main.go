package main

import (
	"fmt"

	. "github.com/deadsy/sdfx/sdf"
)

//-----------------------------------------------------------------------------

func test1() {

	knot := []V2{
		V2{0, 0},
		V2{-1, 1},
		V2{1, 2},
		V2{3, 2},
		V2{4, 1},
		V2{3, 0},
	}

	s_2d := CubicSpline2D(knot)
	p := s_2d.(*CubicSplineSDF2).Polygonize(200)
	p.Render("spline.dxf")
	fmt.Printf("%v\n", s_2d.BoundingBox())
}

//-----------------------------------------------------------------------------

func main() {
	test1()
}


package sdf

//-----------------------------------------------------------------------------

type V3i [3]int
type V2i [2]int

//-----------------------------------------------------------------------------

func (a V2i) SubScalar(b int) V2i {
	return V2i{a[0] - b, a[1] - b}
}

//-----------------------------------------------------------------------------

// Convert V2i to V2
func (a V2i) ToV2() V2 {
	return V2{float64(a[0]), float64(a[1])}
}


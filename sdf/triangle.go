package sdf

//-----------------------------------------------------------------------------

type Triangle struct {
	Vertex1, Vertex2, Vertex3 V3
}

//-----------------------------------------------------------------------------

func (t *Triangle) Normal() V3 {
	e1 := t.Vertex2.Sub(t.Vertex1)
	e2 := t.Vertex3.Sub(t.Vertex1)
	return e1.Cross(e2).Normalize()
}

